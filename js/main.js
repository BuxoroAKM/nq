const nqselect = document.getElementById('nqselect');

function renderList(){
    nqselect.innerHTML = '';
    nq.forEach(element => {
        let item = document.createElement('option');
        item.value = element.id;
        item.innerHTML = element.Nomi;
        nqselect.appendChild(item);
    });
    nqselect.firstElementChild.setAttribute('selected', true);
};

renderList();

